let token = `fa14f0ac3329e45236e7e75fde54670386b2ce5594ccdb2f`

export const server_calls = {
    get: async () => {
        const response = await fetch(`https://dog-inventory.herokuapp.com/api/dogs`,{
            method: 'GET',
            headers: {
                'Content-Type': 'application/json',
                'x-access-token': `Bearer ${token}`
            }
        });

        if (!response.ok){
            throw new Error('Failed to fetch data from server')
        }

        return await response.json()
    },

    create: async(data: any = {}) => {
        const response = await fetch(`https://dog-inventory.herokuapp.com/api/dogs`,{
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'x-access-token': `Bearer ${token}`
            },
            body: JSON.stringify(data)
        });

        if(!response.ok){
            throw new Error('Failed to Create new data on server')
        }

        return await response.json()
    },
    
    update: async (id:string, data:any = {}) => {
        const response = await fetch(`https://dog-inventory.herokuapp.com/api/dogs/${id}`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'x-access-token': `Bearer ${token}`
            },
            body: JSON.stringify(data)
        });
    },

    delete: async(id:string) => {
        const response = await fetch(`https://dog-inventory.herokuapp.com/api/dogs/${id}`,{
            method: 'DELETE',
            headers: {
                'Content-Type': 'application/json',
                'x-access-token': `Bearer ${token}`
            }
        })
    }
}